using System;
using System.Collections.Generic;
using System.Text;

namespace ConsoleApp1
{
    class Ex8
    {
        public int Fibonachi(int n)
        {
            if (n == 0)
            {
                return 0;
            }
            else if (n == 1)
            {
                return 1;
            }
            else
            {
                return Fibonachi(n - 1) + Fibonachi(n - 2);
            }
        }
    }
}
